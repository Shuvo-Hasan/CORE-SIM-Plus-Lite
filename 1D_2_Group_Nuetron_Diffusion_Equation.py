import numpy as np
import matplotlib.pyplot as plt
from scipy.sparse import lil_matrix, csr_matrix
from scipy.sparse.linalg import spsolve

# ============================================================
# GEOMETRY
# ============================================================

L = 100.0
N = 200
dx = L / (N - 1)
x = np.linspace(0, L, N)

n = 2 * N

# ============================================================
# MATERIAL (HETEROGENEOUS)
# ============================================================

mid = N // 2

def mat(i):
    if i < mid:
        return 1.5, 0.4, 0.01, 0.04, 0.08, 0.006, 0.12  # fuel
    else:
        return 1.2, 0.3, 0.002, 0.01, 0.10, 0.0, 0.0     # reflector

# D1, D2, Sa1, Sa2, S12, nuf1, nuf2

# ============================================================
# INDEXING
# ============================================================

def f(i): return i
def t(i): return N + i

# ============================================================
# BUILD FULL OPERATOR MATRIX A = L - S
# ============================================================

A = lil_matrix((n, n))
Fmat = lil_matrix((n, n))

for i in range(N):

    D1, D2, Sa1, Sa2, S12, nuf1, nuf2 = mat(i)

    fi, ti = f(i), t(i)

    if i == 0 or i == N - 1:
        A[fi, fi] = 1.0
        A[ti, ti] = 1.0
        continue

    # ---------------- FAST GROUP ----------------
    A[fi, f(i-1)] = -D1 / dx**2
    A[fi, fi] = 2*D1/dx**2 + Sa1 + S12
    A[fi, f(i+1)] = -D1 / dx**2

    # coupling: thermal -> fast scattering loss (if any)
    # (none here for simplicity)

    # ---------------- THERMAL GROUP ----------------
    A[ti, t(i-1)] = -D2 / dx**2
    A[ti, ti] = 2*D2/dx**2 + Sa2
    A[ti, t(i+1)] = -D2 / dx**2

    # scattering FAST → THERMAL MUST BE INCLUDED AS SOURCE EFFECT
    A[ti, f(i)] = -S12

    # ---------------- FISSION ----------------
    Fmat[fi, fi] += nuf1
    Fmat[fi, ti] += nuf2

A = csr_matrix(A)
Fmat = csr_matrix(Fmat)

# ============================================================
# POWER ITERATION
# ============================================================

phi = np.ones(n)
phi /= np.linalg.norm(phi)

k = 1.0

for it in range(100):

    src = Fmat @ phi

    # scattering correction explicitly added
    phi_new = spsolve(A, src)

    k_new = np.sum(src) / np.sum(Fmat @ phi_new)

    phi_new /= np.linalg.norm(phi_new)

    if abs(k_new - k) < 1e-8:
        break

    phi = phi_new
    k = k_new

print("k_eff =", k)

phi_fast = phi[:N]
phi_thermal = phi[N:]

eps = 1e-14
phi_fast /= max(np.max(phi_fast), eps)
phi_thermal /= max(np.max(phi_thermal), eps)

plt.plot(x, phi_fast, label="Fast")
plt.plot(x, phi_thermal, label="Thermal")
plt.legend()
plt.grid()
plt.title("1D 2-group diffusion")
plt.show()

